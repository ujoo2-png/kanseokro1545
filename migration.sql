-- 1. users (Supabase Auth 연동)
CREATE TABLE IF NOT EXISTS users (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  auth_id uuid UNIQUE REFERENCES auth.users(id) ON DELETE CASCADE,
  username text UNIQUE NOT NULL,
  name text NOT NULL DEFAULT '',
  email text UNIQUE,
  phone text DEFAULT '',
  role text NOT NULL DEFAULT 'tenant' CHECK (role IN ('admin','manager','tenant')),
  unit_id bigint,
  status text NOT NULL DEFAULT 'pending' CHECK (status IN ('active','pending')),
  created_at timestamptz DEFAULT now()
);
ALTER TABLE users ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Users can read own data" ON users FOR SELECT USING (auth.uid() = auth_id OR role = 'admin');
CREATE POLICY "Admins can insert/update/delete" ON users FOR ALL USING (auth.jwt() ->> 'role' = 'admin');

-- 2. buildings
CREATE TABLE IF NOT EXISTS buildings (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL DEFAULT '',
  address text DEFAULT '',
  memo text DEFAULT '',
  admin_name text DEFAULT '',
  admin_phone text DEFAULT '',
  admin_email text DEFAULT '',
  bank_name text DEFAULT '',
  account_holder text DEFAULT '',
  account_number text DEFAULT ''
);
ALTER TABLE buildings ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Authenticated users can CRUD" ON buildings FOR ALL USING (auth.role() = 'authenticated');

-- 3. units
CREATE TABLE IF NOT EXISTS units (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL DEFAULT '',
  building_id bigint REFERENCES buildings(id) ON DELETE SET NULL,
  area numeric DEFAULT 0,
  has_ac boolean DEFAULT false,
  has_tv boolean DEFAULT false,
  has_fridge boolean DEFAULT false,
  has_washer boolean DEFAULT false,
  has_tv_stand boolean DEFAULT false,
  has_bed boolean DEFAULT false,
  has_closet boolean DEFAULT false
);
ALTER TABLE units ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Authenticated users can CRUD" ON units FOR ALL USING (auth.role() = 'authenticated');

-- 4. contracts
